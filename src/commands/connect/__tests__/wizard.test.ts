// T-3: scaffoldItems — hooks
// =============================================================
describe("scaffoldItems: hooks", () => {
  let tmpDir: string;

  beforeEach(() => {
    tmpDir = makeTmpDir();
  });

  afterEach(() => {
    cleanTmpDir(tmpDir);
  });

  it("creates .claude/settings.json with hooks (AC-4)", () => {
    const results = scaffoldItems(tmpDir, ["hooks"], DEFAULT_OPTS);
    expect(results).toHaveLength(1);
    expect(results[0].item).toBe("hooks");
    expect(results[0].filesWritten).toHaveLength(1);

    const settingsPath = path.join(tmpDir, ".claude", "settings.json");
    expect(fs.existsSync(settingsPath)).toBe(true);

    const settings = JSON.parse(fs.readFileSync(settingsPath, "utf-8"));
    expect(settings.hooks).toBeDefined();
    expect(settings.hooks.SessionStart).toBeDefined();
    expect(settings.hooks.SessionStart).toHaveLength(1);
  });

  it("merges hooks into existing settings.json (AC-5)", () => {
    const claudeDir = path.join(tmpDir, ".claude");
    fs.mkdirSync(claudeDir, { recursive: true });

    const existingSettings = {
      permissions: { allow: ["Read"] },
      hooks: {
        PreToolUse: [{ type: "command", command: "echo pre-tool" }],
      },
    };
    fs.writeFileSync(
      path.join(claudeDir, "settings.json"),
      JSON.stringify(existingSettings, null, 2),
      "utf-8"
    );

    scaffoldItems(tmpDir, ["hooks"], DEFAULT_OPTS);

    const settings = JSON.parse(
      fs.readFileSync(path.join(claudeDir, "settings.json"), "utf-8")
    );

    // Existing settings preserved
    expect(settings.permissions.allow).toEqual(["Read"]);
    // Existing hooks preserved
    expect(settings.hooks.PreToolUse).toHaveLength(1);
    expect(settings.hooks.PreToolUse[0].command).toBe("echo pre-tool");
    // New hooks added
    expect(settings.hooks.SessionStart).toHaveLength(1);
  });

  it("does not duplicate hooks on re-run (AC-5)", () => {
    scaffoldItems(tmpDir, ["hooks"], DEFAULT_OPTS);
    scaffoldItems(tmpDir, ["hooks"], DEFAULT_OPTS);

    const settings = JSON.parse(
      fs.readFileSync(path.join(tmpDir, ".claude", "settings.json"), "utf-8")
    );

    expect(settings.hooks.SessionStart).toHaveLength(1);
  });
});

