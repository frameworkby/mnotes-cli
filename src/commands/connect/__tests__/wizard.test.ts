// T-4/T-5: --no-wizard and --all flags via handleClaudeCode
// =============================================================
describe("handleClaudeCode with wizard flags", () => {
  let tmpDir: string;
  let origCwd: () => string;
  let origExit: (code?: number) => never;
  let exitCode: number | undefined;

  beforeEach(() => {
    tmpDir = makeTmpDir();
    origCwd = process.cwd;
    process.cwd = () => tmpDir;
    exitCode = undefined;
    origExit = process.exit;
    process.exit = ((code?: number) => {
      exitCode = code;
      throw new Error(`process.exit(${code})`);
    }) as never;
  });

  afterEach(() => {
    process.cwd = origCwd;
    process.exit = origExit;
    cleanTmpDir(tmpDir);
    vi.restoreAllMocks();
  });

  it("--no-wizard does core setup only, no extras (AC-6)", async () => {
    const configUtils = await import("../config-utils");
    vi.spyOn(configUtils, "validateConnection").mockResolvedValue({ ok: true });

    const { handleClaudeCode } = await import("../index");

    const origLog = console.log;
    console.log = () => {};
    try {
      await handleClaudeCode({
        url: "http://localhost:3000",
        apiKey: "test-key",
        workspace: "ws-123",
        noWizard: true,
      });
    } finally {
      console.log = origLog;
    }

    // Core setup files should exist
    expect(fs.existsSync(path.join(tmpDir, ".mcp.json"))).toBe(true);
    expect(fs.existsSync(path.join(tmpDir, "CLAUDE.md"))).toBe(true);

    // Wizard extras should NOT exist
    expect(fs.existsSync(path.join(tmpDir, ".claude", "settings.json"))).toBe(false);
    expect(fs.existsSync(path.join(tmpDir, ".claude", "skills"))).toBe(false);
    expect(fs.existsSync(path.join(tmpDir, ".claude", "agents"))).toBe(false);
  });

  it("--all installs everything without prompting (AC-7)", async () => {
    const configUtils = await import("../config-utils");
    vi.spyOn(configUtils, "validateConnection").mockResolvedValue({ ok: true });

    const { handleClaudeCode } = await import("../index");

    const origLog = console.log;
    let output = "";
    console.log = (...args: unknown[]) => {
      output += args.join(" ") + "\n";
    };

    try {
      await handleClaudeCode({
        url: "http://localhost:3000",
        apiKey: "test-key",
        workspace: "ws-123",
        all: true,
      });
    } finally {
      console.log = origLog;
    }

    // Core setup
    expect(fs.existsSync(path.join(tmpDir, ".mcp.json"))).toBe(true);
    expect(fs.existsSync(path.join(tmpDir, "CLAUDE.md"))).toBe(true);

    // All extras should exist
    expect(fs.existsSync(path.join(tmpDir, ".claude", "settings.json"))).toBe(true);
    expect(fs.existsSync(path.join(tmpDir, ".claude", "skills", "mnotes-store.md"))).toBe(true);
    expect(fs.existsSync(path.join(tmpDir, ".claude", "skills", "mnotes-recall.md"))).toBe(true);
    expect(fs.existsSync(path.join(tmpDir, ".claude", "agents", "knowledge-manager.md"))).toBe(true);

    // Output should mention extras
    expect(output).toContain("Extras installed:");
  });
});

