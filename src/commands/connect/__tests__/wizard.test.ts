describe("scaffoldItems: skills", () => {
  let tmpDir: string;

  beforeEach(() => {
    tmpDir = makeTmpDir();
  });

  afterEach(() => {
    cleanTmpDir(tmpDir);
  });

  it("creates skill files in .claude/skills/ (AC-4)", () => {
    const results = scaffoldItems(tmpDir, ["skills"], DEFAULT_OPTS);
    expect(results).toHaveLength(1);
    expect(results[0].item).toBe("skills");
    expect(results[0].filesWritten.length).toBeGreaterThan(0);

    const skillsDir = path.join(tmpDir, ".claude", "skills");
    expect(fs.existsSync(path.join(skillsDir, "mnotes-store.md"))).toBe(true);
    expect(fs.existsSync(path.join(skillsDir, "mnotes-recall.md"))).toBe(true);
  });

  it("generated skill files include header (AC-8)", () => {
    scaffoldItems(tmpDir, ["skills"], DEFAULT_OPTS);

    const storeContent = fs.readFileSync(
      path.join(tmpDir, ".claude", "skills", "mnotes-store.md"),
      "utf-8"
    );
    expect(storeContent).toContain("Generated by m-notes CLI");
  });

  it("preserves user-created skill files (AC-5)", () => {
    const skillsDir = path.join(tmpDir, ".claude", "skills");
    fs.mkdirSync(skillsDir, { recursive: true });

    // Write a user-created file with the same name
    fs.writeFileSync(
      path.join(skillsDir, "mnotes-store.md"),
      "# My custom store skill\nCustom content here",
      "utf-8"
    );

    const results = scaffoldItems(tmpDir, ["skills"], DEFAULT_OPTS);

    // The user file should be preserved
    const content = fs.readFileSync(
      path.join(skillsDir, "mnotes-store.md"),
      "utf-8"
    );
    expect(content).toContain("My custom store skill");
    expect(content).not.toContain("Generated by m-notes CLI");

    // The recall skill should still be written (new file)
    expect(fs.existsSync(path.join(skillsDir, "mnotes-recall.md"))).toBe(true);
  });

  it("overwrites m-notes generated skill files on re-run (AC-5)", () => {
    scaffoldItems(tmpDir, ["skills"], DEFAULT_OPTS);

    // Re-run with different workspaceId
    scaffoldItems(tmpDir, ["skills"], { url: "http://new-url.com", workspaceId: "ws-new" });

    const content = fs.readFileSync(
      path.join(tmpDir, ".claude", "skills", "mnotes-store.md"),
      "utf-8"
    );
    expect(content).toContain("ws-new");
  });
});

